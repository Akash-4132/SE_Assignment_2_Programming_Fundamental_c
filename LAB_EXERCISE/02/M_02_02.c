// Q.2 Write a C program that includes variables, constants, and comments. Declare
//     and use different data types (int, char, float) and display their values.

#include <stdio.h>  // Include the standard input/output library



int main() {
    // Declare and initialize variables of different data types
    int age = 25;                // Integer variable
    char grade = 'A';            // Character variable
    float height = 5.9;          

    

    printf("Age: %d\n", age);               // Display the integer value
    printf("Grade: %c\n", grade);           // Display the character value
    printf("Height: %.2f\n", height);       // Display the float value with 2 decimal places
    

 
}
